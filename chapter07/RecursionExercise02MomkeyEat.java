public class RecursionExercise02MomkeyEat {

    public static void main(String[] args) {

        /*
         * 🐒 猴子吃桃问题（经典递归逆推问题）
         *
         * 📘题目描述：
         * 一只猴子在池塘边摘了一堆桃子。
         *
         * 它每天吃桃规则如下：
         * 1️⃣ 每天吃掉“当前桃子的一半 + 1个”
         * 2️⃣ 第10天早上发现，只剩 1 个桃子，并且第10天不再吃桃子
         *
         * ❓问题：
         * 第1天早上，猴子最初有多少个桃子？
         *
         * ------------------------------------------------
         *
         * 🧠核心思路（逆推法）：
         *
         * 我们不从第1天推到第10天，而是：
         * 👉 从第10天往回推第1天
         *
         * 已知：
         * 第10天 = 1个桃子
         *
         * 推导规律：
         *
         * 如果第 day 天有 x 个桃子：
         * 猴子当天吃掉：x/2 + 1
         * 剩下：x - (x/2 + 1)
         * 但逆推更简单：
         *
         * 👉 第 day 天 = (第 day+1 天 + 1) * 2
         *
         * ------------------------------------------------
         *
         * 🔁递归公式：
         *
         * f(day) = (f(day + 1) + 1) * 2
         *
         * 终止条件：
         * f(10) = 1
         *
         * ------------------------------------------------
         *
         * ⚙️递归过程理解：
         *
         * f(1)
         *  ↓
         * f(2)
         *  ↓
         * ...
         *  ↓
         * f(10) = 1
         *
         * 然后回溯计算：
         * f(9) = (1+1)*2
         * f(8) = ...
         */

        T t1 = new T();
        int day = 3;
        int result = t1.monkeyEat(day);
        if (result != -1) {
        	System.out.println("第"+day+"天桃子数量 = " + result);
	
        } else {
        	System.out.println("必须是一到十天");
        
        }
    }
}

class T {

    public int monkeyEat(int day) {

        // 🧨终止条件（递归出口）
        // 第10天只剩1个桃子
        if (day == 10) {
            return 1;
        }

        // 🔁递归逆推核心逻辑
        // 当前天 = (下一天 + 1) * 2
        if (day >= 1 && day <= 9) {
            return (monkeyEat(day + 1) + 1) * 2;
        }

        // ❌非法输入处理
        System.out.println("数值非法");
        return -1;
    }
}